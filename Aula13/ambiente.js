//Loop com while com condição no inicio

var inicio = 1;

while (inicio <= 600) {
    console.log(`Passo ${inicio}`);
    inicio++;
}

//Loop com while com condição no final

var fim = 1;

do{
    console.log(`Passo ${fim}`);
    fim++;
} while(fim <= 600)


//Loop com for 
var p;

for(p = 1; p <= 600; p++){
    console.log(`Passo ${p}`);
}